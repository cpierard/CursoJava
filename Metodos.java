public class Metodos {

  //Ejemplo de método void

  public void saluda() {

    System.out.println("Hola");

  }

  //Ejemplo de método que regresa un valor

  public String saluda2() {

    String cadena = "Hola";
    return cadena;


  }

  public void saludaNombre(String n) {

    System.out.println("Hola " + n);

  }

  public String saludaNombre2(String n) {

    return "Hola " + n;

  }
}
